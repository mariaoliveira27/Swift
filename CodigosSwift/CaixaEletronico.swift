var nota = 100

switch nota {
    case 200 :
        print("Nota  de 200 inserida")
    case 100:
        print("Noat de 100 inserida")
    case 50:
        print("Nota de 50 inserida")
    case 20:
        print("Nota de 20 inserida")
   default:
        print("Nota inválida") 
}

